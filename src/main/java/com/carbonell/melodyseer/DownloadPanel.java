    private String getDownloadSpeedCommand() {
        if (!jFrameMain.getSelectedSpeed().equals("") && !jFrameMain.getSelectedSpeed().equals("Don't limit")) {
            return "-r" + jFrameMain.getSelectedSpeed().split(" ")[0] + "M";
        } else {
            return "";
        }
    }

